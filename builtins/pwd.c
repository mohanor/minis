#include "../minishell.h"

void	ft_pwd(void)
{
	char	s[1024];
	char	*pwd;

	pwd = get_from_env("PWD");
	if (!pwd)
	{
		getcwd(s, 1024);
		pwd = ft_strdup(s);
	}
	printf("%s\n", pwd);
}
